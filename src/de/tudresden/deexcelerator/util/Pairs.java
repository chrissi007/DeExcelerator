package de.tudresden.deexcelerator.util;

/**
 * <span class="de">Paare von Intwerten.</span>
 * <span class="en">Pairs of int values.</span>
 * @author Christopher Werner
 *
 */
public class Pairs {

	public int x;
	public int y;
	
	public void addY () {
		y++;
	}
	
	public void addX () {
		x++;
	}
	
	public void subY () {
		y--;
	}
	
	public void subX () {
		x--;
	}
}
